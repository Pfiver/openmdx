	private final static SortedMap<String,String> failures = new TreeMap<String,String>();
	
	/* (non-Javadoc)
	 * @see java.lang.Thread#run()
	 */
	public void run() {
		for(
		   int i = 0;
		   i < CLASS_COUNT;
		   i++
		){
			try {
				this.classLoader.loadClass(this.classes[i]);
			} catch (Throwable throwable) {
				System.err.println("ClassLoader: " + this.classLoader.getClass().getName());
				signalFailure(this.classes[i], throwable);
			}
		}
	}

	/**
	 * Signal a cloass loading failure
	 * 
	 * @param className
	 * @param throwable
	 */
	private synchronized void signalFailure(
		String className,
		Throwable throwable
	){
		String throwableName = throwable.getClass().getSimpleName();
		System.err.println(Thread.currentThread().getName() + '\t' + throwableName + '\t' + className);
		throwable.printStackTrace();
		failures.put(className, throwableName);
	}

	public static void printFailures () {
		for(Map.Entry<String, String> e : failures.entrySet()) {
			System.out.println(e.getValue() + "\t" + e.getKey());
		}
		System.out.println("Failed classes: " + failures.size());
	}
	
	private static String aClass(){
		return openmdxClasses[
			(int) (Math.random() * openmdxClasses.length)
		];
	}

	@SuppressWarnings("unchecked")
	public static ClassLoader getClassLoader(
		ClassLoader commonClassLoader,
		URL[] enterpriseApplicationArchives
	){
		System.out.println(new Date() + ": Prepare class loader...");
		ClassLoader classLoader = commonClassLoader;
		if(enterpriseApplicationArchives != null) {
			LightweightClassLoader newClassLoader = new LightweightClassLoader(
				"EnterpriseApplication",
				"EnterpriseApplication Class Loader",
				classLoader
			);
			Report report = new Report(
				newClassLoader.getClass().getSimpleName(),
				new VersionNumber("2.0"),
				"EnterpriseApplication"
			);
			newClassLoader.addURLs(enterpriseApplicationArchives, report);
			if(report.isSuccess()) {
				System.out.println(newClassLoader);
			} else {
				System.err.println(report);
			}
			classLoader = newClassLoader;
		}
		try {
			if(TOMCAT) {
				WebappClassLoader webappClassLoader = new WebappClassLoader(
					classLoader
				);
				webappClassLoader.start();
				return webappClassLoader;
			} else {
			    return new LightweightClassLoader(
    	            "WebApp",
    	            "Non-Tomcat Class Loader",
					classLoader
				);
			}
		} catch (RuntimeException e) {
			throw e;
		} catch (Exception e) {
			throw new RuntimeException(e);
		}
	}
				
	public static void launch(
		ClassLoader commonClassLoader,
		URL... enterpriseApplicationArchives
	){
		try {
			System.out.println(new Date() + ": Preparing EAR class loader...");
			ClassLoader classLoader = getClassLoader(
				commonClassLoader, 
				enterpriseApplicationArchives
			);
			System.out.println(new Date() + ": Preparing tests...");
			Runnable[] tests = new Runnable[TEST_COUNT]; 
			for(
				int i = 0;
				i < TEST_COUNT;
				i++
			){
				tests[i] = new ClassLoaderTest(classLoader);
			}
			System.out.println(new Date() + ": Running tests...");
			for(
				int i = 0;
				i < TEST_COUNT;
				i++
			){
				tests[i].run();
			}
			System.out.println(new Date() + ": Tests terminated...");
			ClassLoaderTest.printFailures();
		} catch (Throwable e) {
			System.err.println(
				new BasicException(
					e,
					BasicException.Code.DEFAULT_DOMAIN,
					BasicException.Code.ASSERTION_FAILURE,
					"Class Loader Test Failed",
					null
				)
			);
		}
	}
	
	/**
	 * @param args
	 */
	public static void main(String[] args) {
		launch(
			ClassLoaderTest.class.getClassLoader()
		);
	}

